/**
 * list_len - list_len
 * @h: list_t ptr
 * Return: size_int
 */
size_t listint_len(const listint_t *h)
{
	int i = 0;

	while (h != NULL)
	{
		i++;
		h = h->next;
	}
	return (i);
}
